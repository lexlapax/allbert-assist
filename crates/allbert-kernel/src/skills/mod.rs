const INSTALL_METADATA_FILE: &str = ".allbert-install.toml";

#[derive(Debug, Clone)]
pub struct Skill {
    pub name: String,
    pub description: String,
    pub provenance: SkillProvenance,
    pub intents: Vec<Intent>,
    pub agents: Vec<ContributedAgent>,
    pub scripts: Vec<SkillScript>,
    pub allowed_tools: Vec<String>,
    pub body: String,
    pub path: PathBuf,
}

impl Skill {
    pub fn root_dir(&self) -> Result<&Path, SkillError> {
        self.path
            .parent()
            .ok_or_else(|| SkillError::Load(format!("missing parent for {}", self.path.display())))
    }
}

#[derive(Debug, Clone, Copy, Default, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "kebab-case")]
pub enum SkillProvenance {
    #[default]
    External,
    LocalPath,
    Git,
    SelfAuthored,
}

impl SkillProvenance {
    pub fn label(self) -> &'static str {
        match self {
            Self::External => "external",
            Self::LocalPath => "local-path",
            Self::Git => "git",
            Self::SelfAuthored => "self-authored",
        }
    }
}

