#[derive(Debug, Default, Deserialize)]
#[serde(untagged)]
enum IntentList {
    #[default]
    Missing,
    One(String),
    Many(Vec<String>),
}

impl IntentList {
    fn normalize(self) -> Result<Vec<Intent>, SkillError> {
        let values = match self {
            Self::Missing => Vec::new(),
            Self::One(value) => vec![value],
            Self::Many(values) => values,
        };
        values
            .into_iter()
            .map(|value| {
                Intent::parse(&value)
                    .ok_or_else(|| SkillError::Load(format!("unknown skill intent: {value}")))
            })
            .collect()
    }
}

#[derive(Debug, Default, Deserialize)]
#[serde(untagged)]
enum AgentContributions {
    #[default]
    Missing,
    Paths(Vec<String>),
    Entries(Vec<AgentContributionEntry>),
}

impl AgentContributions {
    fn normalize(&self) -> Vec<String> {
        match self {
            Self::Missing => Vec::new(),
            Self::Paths(paths) => paths.clone(),
            Self::Entries(entries) => entries.iter().map(|entry| entry.path.clone()).collect(),
        }
    }
}

impl ScriptEntries {
    fn entries(&self) -> &[ScriptEntry] {
        match self {
            Self::Missing => &[],
            Self::Entries(entries) => entries,
        }
    }

    fn normalize(&self) -> Result<Vec<SkillScript>, SkillError> {
        self.entries()
            .iter()
            .map(|entry| {
                validate_strict_skill_name(&entry.name)?;
                if entry.interpreter.trim().is_empty() {
                    return Err(SkillError::Load(format!(
                        "script '{}' is missing an interpreter",
                        entry.name
                    )));
                }
                Ok(SkillScript {
                    name: entry.name.clone(),
                    path: entry.path.clone(),
                    interpreter: entry.interpreter.clone(),
                })
            })
            .collect()
    }
}

